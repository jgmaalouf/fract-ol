#include "libft.h"

int	ft_isnum(char *str)
{
	int	i;

	i = 0;
	if (str[i] != '\0' && (str[i] == '-' || str[i] == '+'))
		i++;
	while (str[i] != '\0')
	{
		if ((str[i] < '0' || str[i] > '9') && str[i] != '.')
			return (0);
		i++;
	}
	return (1);
}
